#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = -1;
	c = c % 128;
	if (c == '\0')
		return ((char *)(&s[ft_strlen(s)]));
	while (s[++i])
	{
		if (s[i] == c)
			return ((char *) &s[i]);
	}
	return (0);
}
